#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	i;
	int	summ;
	int	sign;

	i = 0;
	summ = 0;
	sign = 1;
	while ((nptr[i] >= 9 && nptr[i] <= 13) || nptr[i] == ' ')
		i++;
	if (nptr[i] == '-')
	{
		sign = -1;
		i++;
	}
	else if (nptr[i] == '+')
		i++;
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		summ = (10 * summ) + nptr[i] - '0';
		i++;
	}
	return (summ * sign);
}
