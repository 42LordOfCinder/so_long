#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	i;

	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			write(fd, "-", 1);
			n = -n;
		}
		i = n % 10 + 48;
		if (n >= 10)
			ft_putnbr_fd((int)n / 10, fd);
		write(fd, &i, 1);
	}
}
